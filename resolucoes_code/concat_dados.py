# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

# Vamos lá!

# Primeiro, vamos receber os dados do usuário

dado1 = input("Digite o primeiro dado: ")

# Agora, vamos receber o segundo dado do usuário

dado2 = input("Digite o segundo dado:")

# Em seguida, vamos concatenar os dados

resultado = dado1 + " " + dado2

#Por fim, vamos exibir o resultado da concatenação

print("O resultado da concatenação é: ", resultado)